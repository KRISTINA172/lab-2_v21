#include <stdio.h> // Підключення бібліотеки для роботи з введенням/виведенням

int main() {
    int num1, num2; // Оголошення змінних для зберігання введених чисел
    int remainder;  // Змінна для зберігання залишку від ділення

    // Введення чисел користувачем
    printf("Введіть перше ціле число: ");
    scanf("%d", &num1);

    printf("Введіть друге ціле число: ");
    scanf("%d", &num2);

    // Обчислення залишку від ділення
    remainder = num2 % num1;

    // Виведення результату
    printf("Залишок від ділення другого числа на перше: %d\n", remainder);

    return 0; // Завершення програми
}
