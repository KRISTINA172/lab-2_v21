#include <stdio.h>

int main() {
    int a, b;

    // Введення першого числа
    printf("Введіть перше ціле число: ");
    scanf("%d", &a);

    // Введення другого числа
    printf("Введіть друге ціле число: ");
    scanf("%d", &b);

    // Перевірка на ділення на нуль
    if (b == 0) {
        printf("Помилка: ділення на нуль неможливе.\n");
    } else {
        int result = a / b;  // Ціла частина від ділення
        printf("Результат: %d\n", result);
    }

    return 0;
}
